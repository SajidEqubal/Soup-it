package com.shadspace.soup_it.Model;

public class GoodNewsList {

    public String category, ttl, content, wrtnby, tym, rimg;

    public GoodNewsList(String category, String ttl, String content, String wrtnby, String tym, String rimg) {
        this.category = category;
        this.ttl = ttl;
        this.content = content;
        this.wrtnby = wrtnby;
        this.tym = tym;
        this.rimg = rimg;


    }

    public String getCategory3() {
        return category;
    }

    public String getTtl3() {
        return ttl;
    }

    public String getContent3() {
        return content;
    }


    public String getWrtnby3() {
        return wrtnby;
    }


    public String getTym3() {
        return tym;
    }

    public String getRimg3() {
        return rimg;
    }
}

